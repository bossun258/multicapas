    private void cbxpedItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxpedItemStateChanged
       int fila= cbxped.getSelectedIndex();
       String par1= cbxped.getSelectedItem().toString();
        if(fila>0){
          limpiar();
          
          try{
          CallableStatement sp= oCnx.conexion().prepareCall("{call sp_ped(?)}");
          sp.setString(1, par1);
           rs1=sp.executeQuery();
          
              
            while (rs1.next()){
                cliente c = new cliente(rs1.getString(1), rs1.getString(2), "", "");
              empleados e = new empleados(rs1.getString(3), rs1.getString(4), "");
              txtcodcli.setText(c.getIdcli());
              txtrazcli.setText(c.getRazon());
              txtcodemp.setText(e.getIdemp());
              txtapeemp.setText(e.getApeemp());
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }  
        }else{
    limpiar();
        }
    }//GEN-LAST:event_cbxpedItemStateChanged

